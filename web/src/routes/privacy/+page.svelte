<section class="surface panel content-prose">

  <h2>What we don't store</h2>
  <p>
    Cascade is built on Nostr. Your trades, market positions, and discussion posts are recorded on
    public relays — not on Cascade-controlled servers. We don't operate the relays, and we can't
    delete or modify data once it's published there.
  </p>
  <p>
    We don't collect your name, email, phone number, or any personal identifying information beyond
    your Nostr public key.
  </p>

  <h2>Your Nostr key</h2>
  <p>
    Your public key identifies you on the platform. It is a pseudonymous identifier — it does not
    contain your name or contact information by default. If you choose a vanity npub or link your
    key to a personal identity elsewhere, that association is your choice, not ours.
  </p>

  <h2>What we store locally</h2>
  <p>
    Your browser stores minimal preferences locally (display settings, UI state). This data stays
    on your device and is not transmitted to our servers.
  </p>

  <h2>Cookies</h2>
  <p>
    Cascade uses essential session cookies for authentication with the backend. We don't use
    advertising cookies, analytics cookies, or third-party tracking.
  </p>

  <h2>Third parties</h2>
  <p>
    Your trades and posts route through public Nostr relays operated by third parties. Those relays
    have their own data practices. We have no control over and accept no responsibility for what
    relays log or retain.
  </p>

  <h2>Changes</h2>
  <p>
    If this policy changes materially, the update will be reflected here. This is a minimal
    privacy policy appropriate for a public-nostr infrastructure product.
  </p>

  <h2>Contact</h2>
  <p>
    Questions? Reach out via Nostr.
  </p>

